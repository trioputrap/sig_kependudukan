        function ajax_daerah(daerah, id, select_element, master){
            alert('TEST');
            $.ajax({
                url: "https://sigpenduk.herokuapp.com/api/"+ id +"/"+ daerah,
                type: "GET",
                crossDomain: true,
                dataType: "json",                
                processData: false,
                contentType: false,
